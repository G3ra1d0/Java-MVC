    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String[] valores = new String[2];
        valores[0] = (String) pesquisa.getSelectedItem();
        valores[1] = campo.getText();

        ArrayList<String[]> matriz = new ArrayList<>();

        matriz = Controle.recuperaWhere(valores);
        DefaultTableModel TableModel = (DefaultTableModel) tabela.getModel();

        for (int i = 0; i < matriz.size(); i++) {
            TableModel.setValueAt(matriz.get(i)[0], i, 0);
            TableModel.setValueAt(matriz.get(i)[1], i, 1);
            TableModel.setValueAt(matriz.get(i)[2], i, 2);
            TableModel.setValueAt(matriz.get(i)[3], i, 3);
        }

//        this.Codigo.setText(this.vetor[0]);
//        Nome.setText(this.vetor[1]);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel TableModel = (DefaultTableModel) tabela.getModel();
        int linha = tabela.getSelectedRow();
        if (linha > -1) {
            this.Codigo.setText((String) TableModel.getValueAt(linha, 0));
            this.Nome.setText((String) TableModel.getValueAt(linha, 1));
            this.Preco.setText((String) TableModel.getValueAt(linha, 2));
            this.Unidade.setText((String) TableModel.getValueAt(linha, 3));
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma Linha Selecionada!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

